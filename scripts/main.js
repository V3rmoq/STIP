// DOM элементы
const currentLabTitle = document.getElementById('currentLabTitle');
const labCondition = document.getElementById('labCondition');
const resultBtn = document.getElementById('resultBtn');
const codeBtn = document.getElementById('codeBtn');

// Загружаем лабораторную работу
function loadLab(labNumber) {
    const lab = labs[labNumber];
    if (!lab) return;
    
    currentLabTitle.textContent = lab.title;
    labCondition.querySelector('p').textContent = lab.condition;
    resultBtn.href = lab.result;
    codeBtn.href = lab.code;
    labCondition.classList.add('hidden');
}

// Переключаем видимость условия
function toggleCondition() {
    labCondition.classList.toggle('hidden');
}

// Инициализация
document.addEventListener('DOMContentLoaded', () => {
    // Назначаем обработчики для всех ссылок в навигации
    document.querySelectorAll('.navigation a').forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const labNumber = parseInt(link.textContent.match(/\d+/)[0]);
            loadLab(labNumber);
        });
    });
    
    // Загружаем первую лабораторную по умолчанию
    loadLab(2);
});
